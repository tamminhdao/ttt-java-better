package com.tamdao.challenge.Game;

public class GameConfiguration {
    public final String playerOnesType, playerTwosType;
    public final String playerOneSymbol, playerTwoSymbol;

    public GameConfiguration(String playerOnesType, String playerTwosType, String playerOneSymbol, String playerTwoSymbol) {
        this.playerOnesType = playerOnesType;
        this.playerTwosType = playerTwosType;
        this.playerOneSymbol = playerOneSymbol;
        this.playerTwoSymbol = playerTwoSymbol;
    }
}
